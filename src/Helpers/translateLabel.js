export const translateLabel = (label) => {
  if (label === "formula") {
    return "ფორმულა";
  } else if (label === "subField") {
    return "ქვედარგი";
  } else if (label === "field") {
    return "დარგი";
  } else if (label === "sector") {
    return "სქეტორი";
  } else if (label === "minProfit") {
    return "მინ. მოგება";
  } else if (label === "maxProfit") {
    return "მაქს.მოგება";
  } else if (label === "exp" || label === "opExp") {
    return "ხარჯები";
  } else if (label === "profit") {
    return "მოგება";
  } else if (label === "region") {
    return "რეგიონი";
  } else if (label === "question_1") {
    return "კითხვა N1";
  } else if (label === "question_2") {
    return "კითხვა N2";
  } else if (label === "question_3") {
    return "კითხვა N3";
  } else if (label === "question_4") {
    return "კითხვა N4";
  } else if (label === "question_5") {
    return "კითხვა N5";
  }
};
